class AppSpacing {
  AppSpacing._();

  // =============================
  // VERY SMALL SPACING
  // =============================

  /// 4px → Very small spacing
  /// Best for:
  /// - Space between icon and text
  /// - Tight layouts
  static const double xs = 4;

  // =============================
  // SMALL SPACING
  // =============================

  /// 8px → Small spacing
  /// Best for:
  /// - Title & subtitle spacing
  /// - Inside cards
  static const double sm = 8;

  // =============================
  // MEDIUM SPACING
  // =============================

  /// 12px → Medium spacing
  /// Best for:
  /// - Between form fields
  /// - Small sections
  static const double md = 12;

  // =============================
  // LARGE SPACING
  // =============================

  /// 16px → Base spacing
  /// Best for:
  /// - Screen padding
  /// - Card padding
  /// - Main sections
  static const double lg = 16;

  // =============================
  // EXTRA LARGE SPACING
  // =============================

  /// 24px → Large spacing
  /// Best for:
  /// - Page sections
  /// - After titles
  static const double xl = 24;

  // =============================
  // HUGE SPACING
  // =============================

  /// 32px → Very large spacing
  /// Best for:
  /// - Hero sections
  /// - End of pages
  static const double xxl = 32;
}
